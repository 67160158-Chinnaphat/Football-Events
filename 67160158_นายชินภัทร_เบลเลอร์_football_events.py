def read_data_from_csv(file_path):
    """
    Reads data from a CSV file, checks for required columns,
    converts them to numeric, and handles missing values.

    Args:
        file_path (str): Path to the CSV file.

    Returns:
        pandas.DataFrame: DataFrame with data from CSV, or None if an error occurs.
    """
    try:
        df = pd.read_csv(file_path)
        required_cols = ['time', 'side', 'bodypart', 'location', 'situation', 'assist_method', 'fast_break','is_goal']



        for col in required_cols:
            if col not in df.columns:
                raise ValueError(f"Column '{col}' not found in the CSV file.")
            df[col] = pd.to_numeric(df[col], errors='coerce')

        df.dropna(subset=required_cols, inplace=True)
        df.reset_index(drop=True, inplace=True)

        return df

    except FileNotFoundError:
        print(f"Error: File not found at '{file_path}'")
        return None
    except pd.errors.EmptyDataError:
        print(f"Error: CSV file '{file_path}' is empty.")
        return None
    except ValueError as ve:
        print(f"Error: {ve}")
        return None
    except Exception as e:
        print(f"An unexpected error occurred: {e}")
        return None


def plot_with_equation(x_col, y_col, df, color):
    """
    Creates a scatter plot, regression line, and displays the equation.

    Args:
      x_col: The name of the column for the x-axis.
      y_col: The name of the column for the y-axis.
      df: The pandas DataFrame.
      color: The color of the regression line.
    """

    df = df.dropna(subset=[x_col, y_col])

    if df.empty:
        print(f"Skipping plot for {x_col} vs. {y_col} due to missing data.")
        return

    plt.figure(figsize=(8, 6))
    sns.regplot(x=x_col, y=y_col, data=df, scatter_kws={'alpha': 0.5}, line_kws={'color': color})

    model = LinearRegression()
    model.fit(df[[x_col]], df[y_col])

    equation = f'{y_col} = {model.coef_[0]:.2f} * {x_col} + {model.intercept_:.2f}'

    plt.text(0.1, 0.9, equation, transform=plt.gca().transAxes, fontsize=12, color=color)

    plt.title(f'{x_col} vs. {y_col}')
    plt.xlabel(x_col)
    plt.ylabel(y_col)
    plt.grid(True, alpha=0.3)
    plt.show()

