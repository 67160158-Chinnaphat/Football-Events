def read_data_from_csv(file_path):
    """
    Reads data, checks columns, converts to numeric, handles missing values.
    Selects only numeric columns.
    """
    try:
        df = pd.read_csv(file_path)

        required_numeric_cols = ['time', 'side', 'bodypart', 'location', 'situation', 'assist_method', 'fast_break', 'is_goal']

        missing_cols = [col for col in required_numeric_cols if col not in df.columns]
        if missing_cols:
            raise ValueError(f"Missing required columns: {', '.join(missing_cols)}")

        df_numeric = df[required_numeric_cols].copy() # เพิ่ม .copy() เพื่อป้องกันคำเตือน SettingWithCopyWarning

        for col in required_numeric_cols:
            df_numeric[col] = pd.to_numeric(df_numeric[col], errors='coerce')

        df_numeric = df_numeric.dropna()
        df_numeric = df_numeric.reset_index(drop=True)

        return df_numeric

    except FileNotFoundError:
        print(f"Error: File not found at '{file_path}'")
        return None
    except pd.errors.EmptyDataError:
        print(f"Error: CSV file '{file_path}' is empty.")
        return None
    except ValueError as ve:
        print(f"Error: {ve}")
        return None
    except Exception as e:
        print(f"An unexpected error occurred: {e}")
        return None

def create_boxplots(df):
    """
    Creates and displays box plots for each numeric column in the DataFrame.
    """
    for column in df.columns:
        plt.figure(figsize=(16, 4))
        sns.boxplot(x=df[column])
        plt.title(f'Box Plot of {column}')
        plt.xlabel(column)

        # แสดงกราฟบนหน้าจอ
        plt.show()
        plt.close()

