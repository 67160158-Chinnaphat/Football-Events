import pandas as pd

def predict_xg_text(time, side, bodypart, location, situation, assist_method, fast_break):
    """
    ฟังก์ชันทำนาย xG พร้อมระบบค้นหา Top 5 นักเตะที่ยิงสไตล์นี้บ่อยที่สุด
    """
    map_side = {'เหย้า': 1, 'home': 1, 'เยือน': 2, 'away': 2}
    map_bodypart = {'เท้าขวา': 1, 'เท้าซ้าย': 2, 'โหม่ง': 3}

    # 🌟 อัปเดต: เพิ่มตำแหน่งให้ครบทั้ง 19 แบบตาม Data Dictionary
    map_location = {
        'แดนบุก': 1,
        'แดนรับ': 2,
        'กลางกรอบเขตโทษ': 3,
        'ปีกซ้าย': 4,
        'ปีกขวา': 5,
        'มุมแคบและไกล': 6,
        'มุมแคบฝั่งซ้าย': 7,
        'มุมแคบฝั่งขวา': 8,
        'ฝั่งซ้ายของกรอบเขตโทษ': 9,
        'ซ้ายกรอบ 6 หลา': 10,
        'ฝั่งขวาของกรอบเขตโทษ': 11,
        'ขวากรอบ 6 หลา': 12,
        'ยิงจ่อๆ': 13, 'หน้าปากประตู': 13,
        'จุดโทษ': 14,
        'นอกกรอบเขตโทษ': 15,
        'ยิงไกล': 16,
        'ยิงไกลกว่า 35 หลา': 17,
        'ยิงไกลกว่า 40 หลา': 18,
    }

    map_situation = {'โอเพ่นเพลย์': 1, 'เซ็ตพีซ': 2, 'เตะมุม': 3, 'ฟรีคิก': 4}
    map_assist = {'ไม่มี': 0, 'เลี้ยงมาเอง': 0, 'จ่ายปกติ': 1, 'โยน': 2, 'ครอส': 2, 'โหม่งชง': 3, 'จ่ายทะลุช่อง': 4}
    map_fast_break = {'ไม่ใช่': 0, 'ไม่': 0, 'ใช่': 1, 'สวนกลับ': 1}

    def clean_text(text):
        return str(text).strip()

    try:
        side_code = map_side[clean_text(side)]
        body_code = map_bodypart[clean_text(bodypart)]
        loc_code = map_location[clean_text(location)]
        sit_code = map_situation[clean_text(situation)]
        ast_code = map_assist[clean_text(assist_method)]
        fb_code = map_fast_break[clean_text(fast_break)]
    except KeyError as e:
        print(f"\n❌ ระบบไม่รู้จักคำว่า {e} ครับ (โปรดตรวจสอบการสะกดคำให้ตรงกับตัวเลือก)")
        return

    # 1. เตรียมข้อมูลเพื่อส่งให้โมเดลทำนาย
    input_data = pd.DataFrame({
        'time': [time],
        'side': [side_code],
        'bodypart': [body_code],
        'location': [loc_code],
        'situation': [sit_code],
        'assist_method': [ast_code],
        'fast_break': [fb_code]
    })

    # 2. คำนวณ xG (เปลี่ยนเป็น pipeline ตามที่เราเทรนไว้ล่าสุดครับ)
    xg_prob = pipeline.predict_proba(input_data)[0, 1]

    # 3. ค้นหา Top 5 นักเตะจากตาราง data_shot
    similar_shots = data_shot[
        (data_shot['bodypart'] == body_code) &
        (data_shot['location'] == loc_code) &
        (data_shot['situation'] == sit_code) &
        (data_shot['assist_method'] == ast_code) &
        (data_shot['fast_break'] == fb_code)
    ]

    print("\n" + "="*45)
    print("⚽ ผลการวิเคราะห์ Expected Goals (xG) ⚽")
    print("="*45)
    print(f"⏱️ นาทีที่: {time} | 🏟️ เล่นเป็นทีม: {side}")
    print(f"📍 ตำแหน่ง: {location} | 🦵 ใช้: {bodypart}")
    print(f"📋 รูปแบบการเล่น: {situation} | 👟 การส่งบอล: {assist_method} | ⚡ สวนกลับ: {fast_break}")
    print("-" * 45)
    print(f"🎯 โอกาสที่จะเป็นประตู: {xg_prob * 100:.2f}% (ค่า xG = {xg_prob:.4f})")

    # 4. แสดงผล Top 5 นักเตะ
    print("\n🌟 Top 5 นักเตะที่ได้ง้างเท้ายิงสไตล์นี้บ่อยที่สุด 🌟")
    if len(similar_shots) > 0:
        top_players = similar_shots['player'].value_counts().head(5)
        for i, (player, count) in enumerate(top_players.items(), 1):
            print(f"  {i}. {player.title()} : {count} ครั้ง")
    else:
        print("  - ไม่พบสถิตินักเตะที่ยิงด้วยเงื่อนไขเป๊ะๆ แบบนี้ในฐานข้อมูลครับ -")
    print("="*45)

